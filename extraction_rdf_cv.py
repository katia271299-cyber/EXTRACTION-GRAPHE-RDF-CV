import kagglehub
import os
import spacy
import networkx as nx
import matplotlib.pyplot as plt
import pandas as pd
import glob
import re
from rdflib import Graph, Namespace, URIRef, Literal, RDF, RDFS
from rdflib.namespace import FOAF, XSD

# ------------------------------
# 1. Chargement du texte (ex: un CV)
# ------------------------------
path = kagglehub.dataset_download("snehaanbhawal/resume-dataset")
csv_path = glob.glob(os.path.join(path, "**", "Resume.csv"), recursive=True)[0]
df = pd.read_csv(csv_path)
resume_text_column = 'Resume_str' if 'Resume_str' in df.columns else 'resume_str'

cv_index = 1  # Tu peux changer pour tester d'autres CVs
row = df.iloc[cv_index]
resume_text = str(row[resume_text_column])
resume_id = row.get('ID', cv_index + 1)

print(f"Analyse du CV n°{cv_index + 1} (ID: {resume_id})\n")

# ------------------------------
# 2. spaCy pour extraction ML
# ------------------------------
nlp = spacy.load("en_core_web_sm")
doc = nlp(resume_text)

# ------------------------------
# 3. Définition des namespaces RDF
# ------------------------------
EX = Namespace("http://example.org/resume/")
SCHEMA = Namespace("http://schema.org/")
g = Graph()
g.bind("ex", EX)
g.bind("schema", SCHEMA)
g.bind("foaf", FOAF)

# Sujet principal : le CV ou la personne
resume_uri = EX[f"resume_{resume_id}"]
person_uri = EX[f"person_{resume_id}"]

# On lie le CV à une personne (foaf:Person)
g.add((resume_uri, RDF.type, SCHEMA.JobPosting))
g.add((resume_uri, SCHEMA.about, person_uri))
g.add((person_uri, RDF.type, FOAF.Person))

# ------------------------------
# 4. Extraction et création de TRIPLETS RDF
# ------------------------------
# Organisations
for org_text in {ent.text.strip() for ent in doc.ents if ent.label_ == "ORG"}:
    org_uri = EX[f"org_{org_text.replace(' ', '_')}_{hash(org_text) % 10000}"]
    g.add((org_uri, RDF.type, SCHEMA.Organization))
    g.add((org_uri, SCHEMA.name, Literal(org_text, datatype=XSD.string)))
    g.add((person_uri, SCHEMA.alumniOf, org_uri))  # ou worksFor selon contexte
    g.add((person_uri, SCHEMA.knowsAbout, org_uri))  # relation générique

# Langues
for lang in {ent.text.strip() for ent in doc.ents if ent.label_ == "LANGUAGE"}:
    g.add((person_uri, SCHEMA.knowsLanguage, Literal(lang, datatype=XSD.string)))

# Compétences (skills) - via keywords
skill_keywords = ["Python", "Java", "SQL", "Machine Learning", "Deep Learning", "AWS", "Docker", "React", "Excel", "Tableau", "Git"]
for skill in skill_keywords:
    if skill.lower() in resume_text.lower():
        skill_uri = EX[f"skill_{skill.replace(' ', '_')}"]
        g.add((skill_uri, RDF.type, SCHEMA.Skill))
        g.add((skill_uri, RDFS.label, Literal(skill)))
        g.add((person_uri, SCHEMA.hasSkill, skill_uri))

# Rôles (ex: Data Scientist)
role_pattern = re.search(r"\b(Data Scientist|Software Engineer|Machine Learning Engineer|Data Analyst)\b", resume_text, re.I)
if role_pattern:
    role = role_pattern.group(0)
    g.add((person_uri, SCHEMA.jobTitle, Literal(role, datatype=XSD.string)))

# Domaine d'étude
field_pattern = re.search(r"\b(Computer Science|Data Science|Engineering|Business Administration)\b", resume_text, re.I)
if field_pattern:
    field = field_pattern.group(0)
    g.add((person_uri, SCHEMA.alumniOf, Literal(field, datatype=XSD.string)))

# ------------------------------
# 5. Affichage des triplets RDF (exemple)
# ------------------------------
print("=== Exemple de triplets RDF générés ===")
for s, p, o in list(g)[:15]:
    print(f"{s.n3()} → {p.n3()} → {o.n3()}")
print(f"... et {len(g) - 15} autres triplets\n")
print(f"Total : {len(g)} triplets RDF\n")

# ------------------------------
# 6. Visualisation du graphe RDF
# ------------------------------
G_vis = nx.DiGraph()

for s, p, o in g:
    s_label = s.n3().split("/")[-1].strip(">")
    o_label = o if isinstance(o, URIRef) else str(o)
    o_label = o_label.split("/")[-1].strip(">") if isinstance(o, URIRef) else str(o)
    p_label = p.n3().split("/")[-1].split("}")[0].strip(">")

    G_vis.add_node(s_label)
    G_vis.add_node(o_label)
    G_vis.add_edge(s_label, o_label, label=p_label)

plt.figure(figsize=(18, 12))
pos = nx.spring_layout(G_vis, k=1.5, iterations=80, seed=42)

# Couleurs
node_colors = ["#DD6B4D" if "person" in n.lower() or "resume" in n.lower() else "#55A3D9" for n in G_vis.nodes()]
nx.draw_networkx_nodes(G_vis, pos, node_color=node_colors, node_size=2500, edgecolors="black")

nx.draw_networkx_edges(G_vis, pos, arrows=True, arrowstyle="->", arrowsize=20, width=1.5)

nx.draw_networkx_labels(G_vis, pos, font_size=10, font_weight="bold")

edge_labels = nx.get_edge_attributes(G_vis, 'label')
nx.draw_networkx_edge_labels(G_vis, pos, edge_labels=edge_labels, font_color="red", font_size=9)

plt.title("Graphe RDF : Extraction d'entités à partir d'un CV\n(Triplets Sujet → Prédicat → Objet)", fontsize=16)
plt.axis("off")
plt.tight_layout()
plt.show()

# Bonus : Export RDF (pour réutilisation)
g.serialize(destination="resume_knowledge_graph.ttl", format="turtle")
print("Graphe RDF exporté au format Turtle : resume_knowledge_graph.ttl")

