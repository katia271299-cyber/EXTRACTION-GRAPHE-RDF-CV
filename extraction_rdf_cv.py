from rdflib.namespace import FOAF, XSD

# ------------------------------
# 1. Chargement du CV
# ------------------------------
path = kagglehub.dataset_download("snehaanbhawal/resume-dataset")
csv_path = glob.glob(os.path.join(path, "**", "Resume.csv"), recursive=True)[0]
df = pd.read_csv(csv_path)
resume_text_column = 'Resume_str' if 'Resume_str' in df.columns else 'resume_str'

cv_index = 0  # Change ici pour tester un autre CV
row = df.iloc[cv_index]
resume_text = str(row[resume_text_column]).lower()  # tout en minuscule pour les recherches
resume_text_original = str(row[resume_text_column])  # garde l'original pour affichage
resume_id = row.get('ID', cv_index + 1)

print(f"Analyse du CV n°{cv_index + 1} (ID: {resume_id})\n")

# ------------------------------
# 2. spaCy
# ------------------------------
nlp = spacy.load("en_core_web_sm")
doc = nlp(resume_text_original)  # on garde le texte original pour spaCy (meilleure détection)

# ------------------------------
# 3. Namespaces RDF
# ------------------------------
EX = Namespace("http://example.org/resume/")
SCHEMA = Namespace("http://schema.org/")
g = Graph()
g.bind("ex", EX)
g.bind("schema", SCHEMA)
g.bind("foaf", FOAF)

resume_uri = EX[f"resume_{resume_id}"]
person_uri = EX[f"person_{resume_id}"]

g.add((resume_uri, RDF.type, SCHEMA.JobPosting))
g.add((resume_uri, SCHEMA.about, person_uri))
g.add((person_uri, RDF.type, FOAF.Person))

# ------------------------------
# 4. Listes enrichies et améliorées
# ------------------------------

# Compétences techniques et outils (on les cherche en priorité)
skills_list = [
    "python", "java", "javascript", "sql", "excel", "powerpoint", "tableau", "power bi", "r",
    "machine learning", "deep learning", "tensorflow", "pytorch", "aws", "azure", "gcp",
    "docker", "kubernetes", "git", "react", "angular", "node.js", "html", "css",
    "agile", "scrum", "devops", "ci/cd", "linux", "mongodb", "mysql", "postgresql",
    "rest api", "graphql", "jenkins", "terraform", "ansible", "spark", "hadoop", "big data"
]

# Postes / Rôles professionnels
job_titles = [
    "data scientist", "software engineer", "machine learning engineer", "data analyst",
    "data engineer", "full stack developer", "frontend developer", "backend developer",
    "devops engineer", "cloud architect", "project manager", "product manager",
    "business analyst", "ux designer", "ui designer", "system administrator",
    "database administrator", "qa engineer", "security analyst", "hr manager", "recruiter"
]

# Domaines d'études
fields_of_study = [
    "computer science", "data science", "information technology", "software engineering",
    "electrical engineering", "mechanical engineering", "business administration",
    "finance", "economics", "mathematics", "statistics", "physics", "psychology",
    "marketing", "human resources", "organizational leadership", "education",
    "artificial intelligence", "cybersecurity"
]

# ------------------------------
# 5. Extraction améliorée
# ------------------------------

# 1. Compétences (priorité haute : on les ajoute uniquement comme skills)
detected_skills = set()
for skill in skills_list:
    if skill in resume_text:
        detected_skills.add(skill.title())
        skill_uri = EX[f"skill_{skill.replace(' ', '_').replace('-', '_')}"]
        g.add((skill_uri, RDF.type, SCHEMA.Skill))
        g.add((skill_uri, RDFS.label, Literal(skill.title())))
        g.add((person_uri, SCHEMA.knowsAbout, skill_uri))  # plus approprié que hasSkill

# 2. Postes occupés (recherche plus large et multiple)
detected_jobs = set()
for title in job_titles:
    if title in resume_text:
        detected_jobs.add(title.title())
        g.add((person_uri, SCHEMA.jobTitle, Literal(title.title(), datatype=XSD.string)))

# Si aucun trouvé dans la liste, on tente d'extraire via motifs autour de "experience"
# Modifie le regex pour s'assurer que group(1) existe si "experience" est trouvé
experience_section_match = re.search(r'experience\s*(.*?)(?=\s*(?:education|skills|summary|$))', resume_text, re.I | re.DOTALL)
if experience_section_match:
    exp_text = experience_section_match.group(1)
    if exp_text: # Ensure exp_text is not empty before searching within it
        # Cherche des titres en début de ligne ou après des dates
        potential_titles = re.findall(r'(?:^|\n)\s*([A-Za-z\s&]+(?:Engineer|Analyst|Manager|Developer|Scientist|Specialist|Architect|Administrator|Coordinator|Consultant))', exp_text, re.I)
        for t in potential_titles:
            clean_title = t.strip().title()
            if len(clean_title.split()) <= 5:  # évite les phrases trop longues
                detected_jobs.add(clean_title)
                g.add((person_uri, SCHEMA.jobTitle, Literal(clean_title, datatype=XSD.string)))

# 3. Domaines d'études
detected_fields = set()
for field in fields_of_study:
    if field in resume_text:
        detected_fields.add(field.title())
        field_uri = EX[f"field_{field.replace(' ', '_')}"]
        g.add((field_uri, RDF.type, SCHEMA.EducationalOccupationalProgram))
        g.add((field_uri, SCHEMA.name, Literal(field.title())))
        g.add((person_uri, SCHEMA.alumniOf, field_uri))

# 4. Établissements éducatifs (via spaCy + filtre)
education_keywords = ["university", "college", "institute", "school"]
for ent in doc.ents:
    if ent.label_ == "ORG":
        ent_lower = ent.text.lower()
        if any(kw in ent_lower for kw in education_keywords) and "microsoft" not in ent_lower and "excel" not in ent_lower:
            org_uri = EX[f"edu_{ent.text.replace(' ', '_')}_{hash(ent.text) % 10000}"]
            g.add((org_uri, RDF.type, SCHEMA.CollegeOrUniversity))
            g.add((org_uri, SCHEMA.name, Literal(ent.text)))
            g.add((person_uri, SCHEMA.alumniOf, org_uri))

# 5. Organisations professionnelles (expérience pro, hors éducation et faux positifs)
blacklist_org = ["excel", "powerpoint", "word", "outlook", "customer", "state", "responsible", "agile"]
for ent in doc.ents:
    if ent.label_ == "ORG":
        ent_lower = ent.text.lower()
        if (any(kw in ent_lower for kw in education_keywords) or
            any(bl in ent_lower for bl in blacklist_org)):
            continue  # on saute les écoles (déjà traitées) et les faux positifs
        org_uri = EX[f"org_{ent.text.replace(' ', '_')}_{hash(ent.text) % 10000}"]
        g.add((org_uri, RDF.type, SCHEMA.Organization))
        g.add((org_uri, SCHEMA.name, Literal(ent.text)))
        g.add((person_uri, SCHEMA.worksFor, org_uri))  # plus précis que alumniOf

# 6. Affichage résumé
# ------------------------------
print("=== RÉSUMÉ DE L'EXTRACTION ===")
print(f"Compétences détectées : {', '.join(sorted(detected_skills)) or 'Aucune'}")
print(f"Postes occupés : {', '.join(sorted(detected_jobs)) or 'Aucun détecté précisément'}")
print(f"Domaines d'études : {', '.join(sorted(detected_fields)) or 'Aucun'}")
print(f"\nTotal triplets RDF : {len(g)}\n")

# 7. Visualisation améliorée (plus lisible)
# ------------------------------
G_vis = nx.DiGraph()
for s, p, o in g:
    s_label = str(s).split("/")[-1].replace("_", " ")[:30]
    if isinstance(o, URIRef):
        o_label = str(o).split("/")[-1].replace("_", " ")[:30]
    else:
        o_label = str(o)[:30]
    p_label = str(p).split("/")[-1].split("}")[-1]

    G_vis.add_node(s_label)
    G_vis.add_node(o_label)
    G_vis.add_edge(s_label, o_label, label=p_label)

plt.figure(figsize=(20, 14))
pos = nx.spring_layout(G_vis, k=2, iterations=100, seed=42)

# Couleurs par type
node_colors = []
for node in G_vis.nodes():
    if "person" in node.lower() or "resume" in node.lower():
        node_colors.append("#E74C3C")  # rouge pour la personne
    elif "skill" in node.lower():
        node_colors.append("#3498DB")  # bleu pour compétences
    elif "field" in node.lower() or "edu_" in node:
        node_colors.append("#2ECC71")  # vert pour éducation
    elif "org_" in node:
        node_colors.append("#F39C12")  # orange pour entreprises
    else:
        node_colors.append("#95A5A6")

nx.draw_networkx_nodes(G_vis, pos, node_color=node_colors, node_size=3000, edgecolors="black", linewidths=1.5)
nx.draw_networkx_edges(G_vis, pos, arrows=True, arrowstyle="->", arrowsize=20, width=1.2, alpha=0.7)
nx.draw_networkx_labels(G_vis, pos, font_size=9, font_weight="bold")
edge_labels = nx.get_edge_attributes(G_vis, 'label')
nx.draw_networkx_edge_labels(G_vis, pos, edge_labels=edge_labels, font_color="darkred", font_size=8)

plt.title(f"Graphe RDF Amélioré - CV ID {resume_id}\nExtraction plus précise et filtrée", fontsize=18, pad=20)
plt.axis("off")
plt.tight_layout()
plt.show()

